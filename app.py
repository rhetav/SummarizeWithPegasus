from fastapi import FastAPI
import uvicorn
import sys
import os
from fastapi.templating import Jinja2Templates
from starlette.responses import RedirectResponse
from fastapi.responses import Response
from src.SummarizeWithPegasus.pipeline.predicition_pipeline import PredictionPipeline


text:str = "The city council convened yesterday to discuss the proposed park renovation. Residents voiced concerns about the removal of mature trees, while proponents highlighted the need for updated playground equipment and improved accessibility. After two hours of debate, the council decided to postpone the vote until further environmental impact studies are conducted and more community feedback is gathered."

app = FastAPI()

@app.get("/", tags=["authentication"])
async def index():
    return RedirectResponse(url="/docs")



@app.get("/train")
async def training():
    try:
        os.system("python main.py")
        return Response("Training successful !!")

    except Exception as e:
        return Response(f"Error Occurred! {e}")
    



@app.post("/predict")
async def predict_route(text):
    try:

        obj = PredictionPipeline()
        text = obj.predict(text)
        return text
    except Exception as e:
        raise e
    

if __name__=="__main__":
    uvicorn.run(app, host="0.0.0.0", port=8080)
